const LANGUAGE_ALIASES: Record<string, LocalizedBankNameLanguage> = {
  en: "english",
  hi: "hindi",
  te: "telugu",
  ta: "tamil",
  kn: "kannada",
  or: "odia",
  ur: "urdu",
  mr: "marathi",
  bn: "bengali",
  gu: "gujarati",
  pa: "punjabi",
};

export function normalizeBankNameLanguage(language: string): LocalizedBankNameLanguage {
  const normalized = language.trim().toLowerCase();
  if (normalized in LANGUAGE_ALIASES) return LANGUAGE_ALIASES[normalized];
  if (
    normalized === "english" ||
    normalized === "hindi" ||
    normalized === "telugu" ||
    normalized === "tamil" ||
    normalized === "kannada" ||
    normalized === "odia" ||
    normalized === "urdu" ||
    normalized === "marathi" ||
    normalized === "bengali" ||
    normalized === "gujarati" ||
    normalized === "punjabi"
  ) {
    return normalized;
  }
  return "english";
}

export function getLocalizedBankName(bankId: string, language: string): string | undefined {
  const names = LOCALIZED_BANK_NAMES[bankId];
  return names?.[normalizeBankNameLanguage(language)];
}

export function getLocalizedBankSearchTerms(bankId: string): string[] {
  const names = LOCALIZED_BANK_NAMES[bankId];
  return names ? Object.values(names) : [];
}
